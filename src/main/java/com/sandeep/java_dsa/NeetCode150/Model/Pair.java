package com.sandeep.java_dsa.NeetCode150.Model;

public class Pair {
    Object left;
    Object right;

    public Pair(Object left, Object right) {
        this.left = left;
        this.right = right;
    }

    public Object getLeft() {
        return left;
    }

    public void setLeft(Object left) {
        this.left = left;
    }

    public Object getRight() {
        return right;
    }

    public void setRight(Object right) {
        this.right = right;
    }
}
